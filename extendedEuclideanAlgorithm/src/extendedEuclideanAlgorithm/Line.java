package extendedEuclideanAlgorithm;

public class Line {

	private long a;
	private long b;
	private long q;
	private long r;
	private long x;
	private long y;
	public long getA() {
		return a;
	}
	public void setA(long a) {
		this.a = a;
	}
	public long getB() {
		return b;
	}
	public void setB(long b) {
		this.b = b;
	}
	public long getQ() {
		return q;
	}
	public void setQ(long q) {
		this.q = q;
	}
	public long getR() {
		return r;
	}
	public void setR(long r) {
		this.r = r;
	}
	public long getX() {
		return x;
	}
	public void setX(long x) {
		this.x = x;
	}
	public long getY() {
		return y;
	}
	public void setY(long y) {
		this.y = y;
	}
	
}
